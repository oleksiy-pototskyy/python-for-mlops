#!/usr/bin/python3

"""
Simple Calculator - Basic Example for Unit Testing

This module demonstrates the fundamental concept of unit testing:
"If a function adds two numbers, we write a test to confirm that 2 + 3 always returns 5."
"""

def add_numbers(a, b):
    """Add two numbers and return the result.
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        The sum of a and b
    """
    return a + b

def multiply_numbers(a, b):
    """Multiply two numbers and return the result.
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        The product of a and b
    """
    return a * b